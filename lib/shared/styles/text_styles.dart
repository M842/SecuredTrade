TextStyle getPTSandTextStyle({
  double fontSize = 13,
  FontWeight fontWeight = FontWeight.w600,
  Color color = AppColors.primary,
}) {
  return GoogleFonts.ptSans(
    fontSize: fontSize,
    fontWeight: fontWeight,
    color: color,
  );
}

